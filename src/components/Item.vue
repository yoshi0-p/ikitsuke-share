<template>
<div>
  <div class="pre" v-if="$store.state.wanted_pref_id">
      <p>{{$store.state.wanted_pref_id}}のイキツケ一覧
      </p>
   </div>
 <div class="cover">
   
    <div v-for="(value,index) in shares" :key="index">
      <div class="message" v-if= "value.item.pref_id == $store.state.wanted_pref_id">
        <div class="title">
        <p class="text">{{ value.item.shop }}</p>
        <p class="text pref"> {{ value.item.pref_id }} </p>
          <p class="icon" @click="del(index)" alt  v-if = "auth">[×]</p>
        </div>
        <p class="text">{{ value.item.share }}</p>
        <a :href = "value.item.url" class="text url">参考：{{value.item.url}}</a>
        
      </div>
     <!-- <div class="ad" v-if="index%5==0">
        <p>5回に一回</p>
        </div> -->
    </div>
  </div>

</div>
  
